@dataclass(frozen=True)
class AuditTrailEnv:
    surreal_url: str
    surreal_ns: str
    surreal_db: str
    surreal_user: str
    surreal_pass: str
    env_file: Path
    tls_dir: Path


def resolve_secrets_path(secrets_path: str | None = None) -> Path:
    if secrets_path:
        return Path(secrets_path)
    return Path(
        os.environ.get(
            "SECRETS_PATH",
            str(Path.home() / "Documents" / ".secrets" / ".cdb"),
        )
    )


def resolve_env_file(secrets_path: str | None = None) -> Path:
    return resolve_secrets_path(secrets_path) / ENV_FILENAME


def load_env_file(env_file: Path) -> AuditTrailEnv:
    if not env_file.is_file():
        raise FileNotFoundError(f"missing env file: {env_file}")

    values: dict[str, str] = {}
    for line in env_file.read_text(encoding="utf-8").splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#") or "=" not in stripped:
            continue
        key, value = stripped.split("=", 1)
        values[key.strip()] = value.strip()

    missing = [key for key in REQUIRED_ENV_KEYS if not values.get(key)]
    if missing:
        raise ValueError(f"env file missing required keys: {', '.join(missing)}")

    return AuditTrailEnv(
        surreal_url=values["SURREAL_URL"].rstrip("/"),
        surreal_ns=values["SURREAL_NS"],
        surreal_db=values["SURREAL_DB"],
        surreal_user=values["SURREAL_USER"],
        surreal_pass=values["SURREAL_PASS"],
        env_file=env_file,
        tls_dir=env_file.parent / TLS_DIRNAME,
    )


def write_env_file(
    env_file: Path,
    *,
    surreal_url: str,
    surreal_user: str,
    surreal_pass: str,
    surreal_ns: str = DEFAULT_NS,
    surreal_db: str = DEFAULT_DB,
) -> None:
    env_file.parent.mkdir(parents=True, exist_ok=True)
    content = (
        "# T3 governed audit trail endpoint — generated by audit_trail_t3_bootstrap.py\n"
        f"SURREAL_URL={surreal_url.rstrip('/')}\n"
        f"SURREAL_NS={surreal_ns}\n"
        f"SURREAL_DB={surreal_db}\n"
        f"SURREAL_USER={surreal_user}\n"
        f"SURREAL_PASS={surreal_pass}\n"
    )
    env_file.write_text(content, encoding="utf-8")


def write_compose_env_file(
    compose_env_file: Path,
    *,
    surreal_user: str,
    surreal_pass: str,
) -> None:
    compose_env_file.parent.mkdir(parents=True, exist_ok=True)
    compose_env_file.write_text(
        f"SURREAL_USER={surreal_user}\nSURREAL_PASS={surreal_pass}\n",
        encoding="utf-8",
    )


def detect_private_lan_ipv4() -> str:
    candidates: list[str] = []

    if sys.platform == "win32":
        try:
            proc = subprocess.run(
                [
                    "powershell",
                    "-NoProfile",
                    "-Command",
                    (
                        "Get-NetIPAddress -AddressFamily IPv4 | "
                        "Where-Object { $_.IPAddress -notlike '127.*' -and "
                        "$_.PrefixOrigin -ne 'WellKnown' } | "
                        "Sort-Object InterfaceMetric | "
                        "Select-Object -ExpandProperty IPAddress"
                    ),
                ],
                capture_output=True,
                text=True,
                check=False,
                timeout=20,
            )
            if proc.returncode == 0:
                candidates.extend(
                    line.strip()
                    for line in proc.stdout.splitlines()
                    if line.strip()
                )
        except (OSError, subprocess.TimeoutExpired):
            pass

    try:
        probe = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        probe.connect(("8.8.8.8", 80))
        candidates.append(probe.getsockname()[0])
    except OSError:
        pass
    finally:
        try:
            probe.close()
        except Exception:
            pass

    for candidate in candidates:
        try:
            ip = ipaddress.ip_address(candidate)
        except ValueError:
            continue
        if ip.is_loopback or not ip.is_private:
            continue
        return str(ip)

    raise RuntimeError("no private LAN IPv4 detected for non-localhost T3 URL")


def guard_non_localhost(url: str) -> str:
    parsed = urllib.parse.urlparse(url)
    host = (parsed.hostname or "").lower()
    if host in LOCALHOST_HOSTS:
        raise ValueError("T3 endpoint must not use localhost")
    if parsed.scheme not in {"https"}:
        raise ValueError("T3 endpoint must use HTTPS")
    return host


