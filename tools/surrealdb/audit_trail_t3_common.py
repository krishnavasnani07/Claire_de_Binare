def _resolve_openssl_executable() -> str:
    candidates = ["openssl"]
    if sys.platform == "win32":
        candidates.extend(
            [
                r"C:\Program Files\Git\usr\bin\openssl.exe",
                r"C:\Program Files (x86)\Git\usr\bin\openssl.exe",
            ]
        )
    for candidate in candidates:
        try:
            proc = subprocess.run(
                [candidate, "version"],
                capture_output=True,
                text=True,
                check=False,
            )
            if proc.returncode == 0:
                return candidate
        except OSError:
            continue
    raise RuntimeError("openssl not found; install OpenSSL or Git for Windows")


def generate_tls_material(tls_dir: Path, *, san_ip: str) -> tuple[Path, Path]:
    tls_dir.mkdir(parents=True, exist_ok=True)
    cert_path = tls_dir / "cert.pem"
    key_path = tls_dir / "key.pem"
    if cert_path.is_file() and key_path.is_file():
        return cert_path, key_path

    openssl = _resolve_openssl_executable()
    cmd = [
        openssl,
        "req",
        "-x509",
        "-newkey",
        "rsa:2048",
        "-keyout",
        str(key_path),
        "-out",
        str(cert_path),
        "-days",
        "365",
        "-nodes",
        "-subj",
        "/CN=cdb-audit-trail-t3",
        "-addext",
        f"subjectAltName=IP:{san_ip},IP:127.0.0.1",
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True, check=False)
    if proc.returncode != 0:
        raise RuntimeError(
            "openssl TLS generation failed; ensure OpenSSL is installed"
        )
    key_path.chmod(0o600)
    return cert_path, key_path


def build_ssl_context(cert_path: Path) -> ssl.SSLContext:
    context = ssl.create_default_context()
    context.load_verify_locations(cafile=str(cert_path))
    context.check_hostname = False
    context.verify_mode = ssl.CERT_REQUIRED
    return context


def endpoint_fingerprint(*, ns: str, db: str, mtls_policy: str = "optional") -> str:
    payload = f"endpoint_class=governed_non_localhost_T3|ns={ns}|db={db}|tls=1|mtls={mtls_policy}"
    return hashlib.sha256(payload.encode("utf-8")).hexdigest()


def health_check(url: str, *, ssl_context: ssl.SSLContext | None = None) -> bool:
    try:
        req = urllib.request.Request(f"{url.rstrip('/')}/health", method="GET")
        with urllib.request.urlopen(req, timeout=5, context=ssl_context) as resp:
            return resp.status == 200
    except Exception:
        return False


def sql_request(
    env: AuditTrailEnv,
    sql: str,
    *,
    ssl_context: ssl.SSLContext | None = None,
    ns: str | None = None,
    db: str | None = None,
) -> tuple[int, bytes]:
    token = base64.b64encode(
        f"{env.surreal_user}:{env.surreal_pass}".encode()
    ).decode()
    headers = {
        "Accept": "application/json",
        "Content-Type": "text/plain",
        "Authorization": f"Basic {token}",
        "surreal-ns": ns or env.surreal_ns,
        "surreal-db": db or env.surreal_db,
    }
    req = urllib.request.Request(
        f"{env.surreal_url}/sql",
        data=sql.encode("utf-8"),
        headers=headers,
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=30, context=ssl_context) as resp:
            return resp.status, resp.read()
    except urllib.error.HTTPError as exc:
        return exc.code, exc.read()


def check_statement_results(body: bytes) -> list[dict]:
    raw = body.decode("utf-8", errors="replace")
    if not raw.strip():
        raise ValueError("empty /sql response body")
    results = json.loads(raw)
    if not isinstance(results, list):
        raise ValueError("expected JSON array from /sql")
    for idx, item in enumerate(results):
        if not isinstance(item, dict):
            raise ValueError(f"statement {idx}: expected object")
        if item.get("status") != "OK":
            detail = item.get("detail") or item.get("result") or item
            raise ValueError(f"statement {idx} failed: {detail!s}"[:300])
    return results


def container_network_names(container_name: str) -> tuple[bool, set[str]]:
    proc = subprocess.run(
        [
            "docker",
            "inspect",
            container_name,
            "--format",
            "{{range $k, $v := .NetworkSettings.Networks}}{{$k}} {{end}}",
        ],
        capture_output=True,
        text=True,
        check=False,
    )
    if proc.returncode != 0:
        return False, set()
    return True, {part for part in proc.stdout.strip().split() if part}


def redact_output(text: str, env: AuditTrailEnv) -> str:
    redacted = text
    for secret in (
        env.surreal_pass,
        env.surreal_user,
        env.surreal_url,
        urllib.parse.urlparse(env.surreal_url).hostname or "",
    ):
        if secret:
            redacted = redacted.replace(secret, "[REDACTED]")
    redacted = re.sub(r"https?://[^\s\"']+", "[REDACTED_URL]", redacted)
    redacted = re.sub(
        r"\b(?:\d{1,3}\.){3}\d{1,3}\b", "[REDACTED_IP]", redacted
    )
    return redacted
